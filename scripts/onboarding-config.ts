export type GitHubQuery = {
  name: string;
  query: string;
  limit: number;
};

export type BountyPlatform = "algora" | "polar" | "issuehunt" | "gitcoin";

export type OnboardingAnswers = {
  minimumAmountUsd: number;
  preferredAmountUsd: number;
  maxActiveBounties: number;
  maxResearchMinutesBeforeDecision: number;
  maxExpectedFixHours: number;
  defaultDecisionThreshold: number;
  prefer: string[];
  avoid: string[];
  requireExplicitPayout: boolean;
  requireOpenIssue: boolean;
  requireNoExistingSolvingPr: boolean;
  requireUserApprovalBeforeClaiming: boolean;
  requireUserApprovalBeforePosting: boolean;
  githubQueries: GitHubQuery[];
  enabledPlatforms: BountyPlatform[];
  includeRepos: string[];
  excludeRepos: string[];
  includeLanguages: string[];
  excludeLanguages: string[];
  minimumSearchAmountUsd: number;
};

export type WritePolicy = "preserve" | "overwrite";
export type WritePlan = { action: "skip" | "write"; path: string };

